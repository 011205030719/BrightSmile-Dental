            <div class="infection-control-content">
                <h2>How Do We Practice Infection Control at Nuffield Dental?</h2>
                <ul style="width: 1150px; font-size:16px;">
                    <li>All dentists and assistants wear clean personal protective equipment—a scrub apron, eyewear, disposable gloves, and face masks;</li>
                    <li>All treatment instruments are wrapped and autoclaved; steamed with pre-vacuum-type sterilisers prior to usage;</li>
                    <li>All sterilisations are tracked by systematically logging information such as the clinician involved in packing or sterilising the equipment at the different points of the sterility cycle;</li>
                    <li>Dental chairs are cleansed with disinfectant before and after every treatment procedure;</li>
                    <li>Patients are given a protective eye protector to protect their eyes during dental treatments;</li>
                    <li>Antimicrobial mouth rinses are provided just before a dental procedure to reduce contamination;</li>
                    <li>During surgeries, patients’ faces are protected with a sterilised face mask. Solely part of the cheek, chin, lips are disinfected using Iodine;</li>
                    <li>Disinfection is carried out with chlorhexidine mouthwash after the surgery.</li>
                </ul>
                <p>We ensure that these protocols are strictly adhered to by all dentists and clinic staff.</p>
