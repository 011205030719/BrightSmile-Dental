                <h2 style="font-size: 30px; color: #e74d06; margin-bottom: 20px;">Self-Aspirating Syringes:</h2>
                <p style="color: #444; line-height: 36px; font-size: 16px; width: 1020px;">
                    The use of self-aspirating syringes is a novel addition to the dentistry world in the past decade. 
                    With the use of this syringe, the injection of local anaesthetic into your bloodstream is controlled and specific.
                    The syringe has an ergonomic design which provides optimal balance, tactile control of the injection and lowered hand stress during the injection.
                </p>
                <br>
                <p style="color: #444; line-height: 36px; font-size: 16px;width: 1020px;">
                    In self-aspirating needles and syringe system, aspiration can occur solely after the injection has begun. Automatic aspiration occurs by light pressure on the plunger. The self-aspiration without retraction minimises the risk of needle tip movement in the tissue and encourages the slow release of epinephrine (local anaesthetic). With the use of such syringes, there is a reduced chance of unexpected cardiovascular events in the compromised patients depending on the amount of epinephrine. If you have experienced or are currently experiencing cardiovascular problems, please inform our staff before proceeding with any dental procedures.
                </p>
